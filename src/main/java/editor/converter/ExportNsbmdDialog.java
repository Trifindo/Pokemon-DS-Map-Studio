    private void jbImdBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbImdBrowseActionPerformed
        final JFileChooser fc = new JFileChooser();
        File folder = new File(Utils.removeExtensionFromPath(handler.getMapMatrix().filePath)).getParentFile();
        fc.setCurrentDirectory(folder);
        //fc.setSelectedFile(folder);
        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        fc.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                try {
                    return f.isDirectory() || f.getName().endsWith(".imd");
                } catch (Exception ex) {
                    return false;
                }
            }

            @Override
            public String getDescription() {
                return "Folder or IMD file (*.imd)";
            }
        });
        fc.setApproveButtonText("Select folder");
        fc.setDialogTitle("Select the folder that contains the IMD files");

        final int returnValOpen = fc.showOpenDialog(this);
        if (returnValOpen == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            if (file.exists()) {
                String folderPath;
                if (file.isDirectory()) {
                    folderPath = file.getPath();
                } else {
                    folderPath = file.getParent();
                }
                loadImdFilesFromFolder(folderPath);
            }
        }
    }//GEN-LAST:event_jbImdBrowseActionPerformed

    private void jbAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAcceptActionPerformed
        if (!new File(imdFolderPath).exists()) {
            JOptionPane.showMessageDialog(this,
                    "Please select a valid folder that contains the IMD files.",
                    "Invalid IMD folder",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!new File(nsbFolderPath).exists()) {
            JOptionPane.showMessageDialog(this,
                    "Please select a valid output folder for exporting the NSBMD files.",
                    "Invalid NSBMD folder",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultListModel<JCheckBox> model = (DefaultListModel<JCheckBox>) jScrollCheckboxList.getCheckboxList().getModel();
            selectedObjNames = new ArrayList<>(model.getSize());
            for (int i = 0; i < model.getSize(); i++) {
                if (model.get(i).isSelected()) {
                    selectedObjNames.add(model.get(i).getText());
                }
            }
            if (selectedObjNames.size() > 0) {
                returnValue = APPROVE_OPTION;
                dispose();
            } else {
                JOptionPane.showMessageDialog(this,
                        "There are no IMD selected for converting into NSBMD.\n"
                                + "Select at least one IMD model from the list.", "No IMD models selected",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbAcceptActionPerformed

