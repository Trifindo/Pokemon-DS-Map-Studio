    public void init(MapEditorHandler handler) {
        this.handler = handler;

        String imdFolderPath = new File(handler.getMapMatrix().filePath).getParent();
        loadImdFilesFromFolder(imdFolderPath);

        String nsbFolderPath = new File(Utils.removeExtensionFromPath(handler.getMapMatrix().filePath)).getParent();
        if (isFolderPathValid(nsbFolderPath)) {
            this.nsbFolderPath = nsbFolderPath;
            jtfNsbFolderPath.setText(nsbFolderPath);
        }

    }

    private boolean isFolderPathValid(String folderPath) {
        try {
            File file = new File(folderPath);
            return file.isDirectory();
        } catch (Exception ex) {
            return false;
        }
    }

    private void loadImdFilesFromFolder(String folderPath) {
        try {
            File folder = new File(folderPath);
            File[] files = folder.listFiles(new FilenameFilter() {
                @Override
                public boolean accept(File dir, String name) {
                    return name.endsWith(".imd");
                }
            });

            DefaultListModel<JCheckBox> model = new DefaultListModel();
            this.jScrollCheckboxList.getCheckboxList().setModel(model);
            for (File file : files) {
                model.addElement(new JCheckBox(file.getName()));
            }

            for (int i = 0; i < model.getSize(); i++) {
                model.get(i).setSelected(hasMatrixCoordsInImdName(model.get(i).getText()));
            }
            jScrollCheckboxList.getCheckboxList().setModel(model);

            imdFolderPath = folderPath;
            jtfImdFolderPath.setText(folderPath);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private boolean hasMatrixCoordsInImdName(String imdName) {
        String name = Utils.removeExtensionFromPath(imdName);
        try {
            String[] splitName = name.split("_");
            return (hasCoordInName(splitName[splitName.length - 2])
                    && hasCoordInName(splitName[splitName.length - 1]));
        } catch (Exception ex) {
            return false;
        }
    }

    private boolean hasCoordInName(String name) {
        try {
            Integer.parseInt(name);
            return true;
        } catch (Exception ex) {
            return false;
        }
    }

